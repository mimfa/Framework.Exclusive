        public static Color InvertColor(Color color)
        {
            Color invertedColor = Color.FromArgb(color.ToArgb() ^ 0xffffff);

            if (invertedColor.R > 110 && invertedColor.R < 150 &&
                invertedColor.G > 110 && invertedColor.G < 150 &&
                invertedColor.B > 110 && invertedColor.B < 150)
            {
                int avg = (invertedColor.R + invertedColor.G + invertedColor.B) / 3;
                avg = avg > 128 ? 200 : 60;
                invertedColor = Color.FromArgb(avg, avg, avg);
            }
            return invertedColor;
        }
        public static Color ChangeColor(Color color, short tolerance = 5, short alpha = 255)
        {
            int red = color.R;
            int green = color.G;
            int blue = color.B;

            red = red + tolerance;
            green = green + tolerance;
            blue = blue + tolerance;

            if (red > 255 || red < 0) red -= (tolerance * 2);
            if (green > 255 || green < 0) green -= (tolerance * 2);
            if (blue > 255 || blue < 0) blue -= (tolerance * 2);

            red = red > 255 ? color.R > 127 ? 0 : 255 : red < 0 ? color.R > 127 ? 0 : 255 : red;
            green = green > 255 ? color.G > 127 ? 0 : 255 : green < 0 ? color.G > 127 ? 0 : 255 : green;
            blue = blue > 255 ? color.B > 127 ? 0 : 255 : blue < 0 ? color.B > 127 ? 0 : 255 : blue;

            return Color.FromArgb(alpha, red, green, blue);
        }
        public static Color HighContrastColor(Color color)
        {
            int c = color.R + color.G + color.B;
            return c < 383 ? Color.FromArgb(255, 255, 255, 255) : Color.FromArgb(255, 0, 0, 0);
        }

        public static Color GetRandomColor(int? seed = null, short min = 0, short max = 255)
        {
            return GetRandomColor(seed.HasValue ? new Random(seed.Value) : new Random());
        }
        public static Color GetRandomColor(Random r, short min = 0, short max = 255)
        {
            return Color.FromArgb(r.Next(min, max), r.Next(min, max), r.Next(min, max));
        }
        public static Color GetPercentColor(decimal percent, int changeRange = 7)
        {
            int length = Math.Min(GreenRange.Length, changeRange);
            int unit = 100 / length;
            int index = Math.Abs(Convert.ToInt32(percent / unit));
            if (percent > 0) return GreenRange[index];
            else if (percent < 0) return RedRange[index];
            else return Color.White;
        }

