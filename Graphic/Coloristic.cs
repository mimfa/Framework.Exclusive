        public static void AllChildsControlColorChange(Control parent, OrderMode selectLow = OrderMode.Random, params Color[] colorRange)
        {
            Color[] arrc = GetColorRenge(parent.Controls.Count, selectLow, colorRange);
            for (int i = 0; i < parent.Controls.Count; i++)
                parent.Controls[i].BackColor = arrc[i];
        }
        public static void AllChildsControlColorChange(Control parent, ColorRange colorRange, OrderMode selectLow = OrderMode.Random)
        {
            AllChildsControlColorChange(parent, selectLow, GetColorRenge(colorRange, OrderMode.OrderBy));
        }
        public static Color[] GetColorRenge(int length, OrderMode selectLow = OrderMode.Random, params Color[] colorRange)
        {
            Color[] arrc = new Color[length];
            int j = 0;
            for (int i = 0; i < length; i++)
            {
                if (colorRange.Length > j)
                    arrc[i] = colorRange[j++];
                else
                {
                    j = 0;
                    arrc[i] = colorRange[j++];
                }
            }
            switch (selectLow)
            {
                case OrderMode.OrderBy:
                    return arrc;
                case OrderMode.Random:
                    return CollectionService.Shake(arrc);
                case OrderMode.Special:
                    return CollectionService.Shake(CollectionService.Shake(arrc));
                default:
                    return arrc;
            }
        }
        public static Color[] GetColorRenge(int length, ColorRange colorRange, OrderMode selectLow = OrderMode.Random)
        {
            return GetColorRenge(length, selectLow, GetColorRenge(colorRange, OrderMode.OrderBy));
        }
